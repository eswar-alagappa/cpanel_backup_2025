<h2 class="h2-heading heading_color">ALAGAPPA SPORTS OLYMPIAD </h2>
<p>Alagappa Sports Olympiad (Annual Sports Meet) of Alagappa Group of Educational Institutions, Karaikudi was held on 5th and 6th January 2018. On 5th January the function was inaugurated by Prof. Subbiah, The Vice Chancellor of Alagappa University, Karaikudi. On this occasion March Past was performed by the students of Alagappa Group of Educational Institutions, Karaikudi and Chennai.</p>
<p>On 6th January, 2018 Sports and Games were conducted for the students of Alagappa Matriculation Hr. Sec. School, Alagappa Montessori School and Alagappa Primary School. A small contingent of athletes from our Alagappa School Chennai was there to showcase their talent competing with our senior students.  On this occasion Dr. M. Sundar, the Principal, Alagappa College of Physical Education, Karaikudi was the Chief Guest. The function was presided over by Dr. R. Vairavan, the Chairman of the Alagappa Group of Educational institutions and Trust. To grace the occasion Ms. Devi Vairavan, one of the daughters of our Chairman was present.</p>
<p>The students did March Past and Guard of Honour was given to the Chairman of the Alagappa Group of Educational institutions and of the Trust and the Chief Guest of the function by our NCC Cadets. A colorful cultural feast was given to the audience by our students. Many events were conducted viz., 100 and 200 meter  race, relay race for high  and higher secondary students. Our students demonstrated martial arts along with Yoga demonstration.</p>
<p>To describe the whole event in   a nutshell it was an electrifying movement for all who were present there. They enjoyed each and every moment of the programme expressing their exuberance with thundering applause at the end of every event.</p>
