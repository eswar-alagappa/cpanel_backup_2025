<h2 class="h2-heading heading_color">Pongal Celebrations 2016</h2>
<p>Pongal is a four-day harvest festival of Tamilnadu, celebrated with gusto and fervor, by the Tamil in state. The festival is dedicated to Mother Nature, as farmers thank her for blessing them with bountiful crop. Each day of the four-day festival bears significance in one way or the other.</p>
<h3>Day One – Bhogi</h3>
<p>The celebration of pongal festival start with Bhogi / Bogi. One day prior to Bhogi, the households are clean, so that the premises become pious for the celebrations. People worship sun god and agricultural equipments that are used to cut the crop.</p>
<h3>Day Two – Surya Pongal</h3>
<p>The Second day is the main day of Pongal Festival and is celebrated as Surya Pongal. A new clay pot is used for the purpose. The rice is cooked with milk and jaggery in an open area. Until the rice falls. This is termed as Pongal. The Pongal thus made is offered to sun god.</p>
<h3>Day Three – Mattu Pongal</h3>
<p>Mattu pongal is the third day of the festival. This day is dedicated to farm animals including cow and ox. On Mattu Pongal, the farm animals are bathed and then adorned in a beautiful way. Their homes are painted and garland is wrapped around the horns and apply tilak on the forehead. The farmers often take them round in  the entire village.</p>
<h3>Day Four – Kaanum Pongal</h3>
<p>The fourth and the last day of pongal is known as Kaanum Pongal. It is the time for people to pay visits to their loved ones and faraway acquaintances. Pongal songs and folk dances are performed by people, on Kaanum Pongal.</p>
<p>In our school we celebrated Pongal on the 14th of January 2016. It was a joyous and filled our days a head with happiness. In the assembly area of our school, a hut was built which resembles  the village life. The entire place was decorated in the traditional method with thoranam. In front of the hut house the fire was lit , mud pot was decorated and pongal was prepared. Special Pooja was performed in which all staff members took part. Pongal with sugarcane was distributed to all faculty members.</p>
<h3>Cultural Programme</h3>
<p>To make the celebrations more colourful and remarkable a cultural feast was given by the students of our school. Folk dance was presented by Girls and the Keyboard was played by our students.</p>
<p>A special traditional game show was organized for our faculty members. Uri Adi was organized. Both male and female faculties took part in the game show. Our P.E.T Faculty Ms Illayarani won the show and she was awarded with a cash prize.</p>

