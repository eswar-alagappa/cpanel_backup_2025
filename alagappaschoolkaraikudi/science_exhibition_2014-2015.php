<h2 class="h2-heading heading_color">Science Exhibition</h2>
<p>The Department of School Education, Tamilnadu, organized A District Level Science Exhibition for the schools in Sivagangai District. The Venue for this exhibition was our Alagappa Matriculation Hr Sec School, Auditorium. More than 25 schools took part in this exhibition.</p>
<p>Our Principal Mr Kumaran Ganesan was requested to inaugurate the exhibition by the organizers and he declared the exhibition open. On this occasion The District Education Officer and the head masters of various schools were present.</p>
<p>Our Principal, Mr Kumaran Ganesan  visited the entire stall which was exhibited, the participants explained the concept very enthusiastically. Our Principal also questioned them and they were able to give the answers. He appreciated the participants and wished them a great success.</p>
<p>Our faculty Mr P. Prabaharan, Head of the Department of Science, was also asked to exhibit his project. This level was meant only for the teachers. If his project gets success, he will be allowed to present his exhibit in the State Level. Our Principal and the Headmasters of various schools wished him for the success.</p>
