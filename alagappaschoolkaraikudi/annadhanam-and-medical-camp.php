<h2 class="h2-heading heading_color">Annadhanam and Medical Camp for Palani Pilgrims</h2>
<p>Interact Club is the students wing of Rotary Club. Our school also has Interact Club which extends its services to the needy people in and around Karaikudi. As a part of the service this year Interact Club of Alagappa Matriculation Hr. Sec. School, Karaikudi, organized Annadhanam and Free Medical Camp for Palani Pilgrims. Medical Camp was organized in association with Padmini Urology Clinic and Karaikudi Shirdi Sai Trust.</p>
<p>On 16.1.2016 our students Interactors, Principal and Staff Members of our school distributed green Towel and Dhoti to the pilgrims carrying Kavadi at Patharakudi Junction, at the outskirts of Karaikudi. Also a special abishekam was performed to the Vel carried by the pilgrim to invoke the blessings of Lord Muruga.</p>
<p>On 20.1.2016, a team of 10 teachers, 25 students consisting of 20 interactors, 5 NCC cadets along with the Principal went to Dindigul to perform Annadhanam to the Palani Pilgrims. Also a Medical team consisting of 7 nurses, 2 doctors and 1 physiotherapist from a renowned hospital Padmini Urology, joined hands with Alagappa Matric. Team that rendered free Medical checkup to the pilgrims. For this project a big and spacious Marriage hall namely Subamangala Mahal was booked. </p>
<p>Around 3000 pilgrims were served tasty food. The pilgrims were very happy and were fully satisfied. About 2000 pilgrims got free Medical checkup. Physiotherapist was assisted by our student volunteers. They gave oil massage to the legs of pilgrims, for which our school and our students were blessed by the pilgrims. Also the pilgrims were given normal tablets like paracetemol, digene and massage oil bottles free of cost for their future use. </p>
<p>The project was very successful and appreciated by the people from different walks of life and field. It gave a great satisfaction that we have done something worthy to the society and would like to continue to do in the years to come with the blessings of god and with the support of the management.</p>
