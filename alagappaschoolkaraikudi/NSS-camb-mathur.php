<h2 class="h2-heading heading_color">NSS CAMP - MATHUR</h3>
<p>National Service Scheme, under the Ministry of Youth Affairs and Sports Government of India, popularly known as NSS was launched in Gandhiji's Birth Centenary Year 1969, in 37 Universities involving 40,000 students with primary focus on the development of personality of students through community service.</p>
<p>Today, NSS has more than 3.2 million student volunteers on its roll spread over 298 Universities and 42 (+2) Senior Secondary Councils and Directorate of Vocational Education all over the country. More than 3.75 crores students from Universities, Colleges and Institutions of higher learning have benefited from the NSS activities, as student volunteers.</p>
<p>Our school has also got NSS which is functioning very actively by involving the higher secondary students as the volunteers. From the date of its inception our school NSS volunteers have performed various services through which people in and around Karaikudi got benefited. As part of its service our volunteers went on rally through the town that created awareness on Dengue, on ill effects of tobacco, on road safety etc. </p>
<p>Apart from the social service every year our student volunteers choose any one of the temple around Karaikudi town and clean its premises. This year Swayam Prakasheswarar Temple, Mathur was selected for the purpose. The temple is very famous for the Lord Bairavar as one of the deities in the sivan temple. Our student volunteers got the permission from the Temple trust for this project. Mr. Madhavan, the NSS Coordinator along with 25 students from XI standard took part in this project. They got lots of appreciation for the service rendered, from the residents of Mathur village including the trust members.</p>
