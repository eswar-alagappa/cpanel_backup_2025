<h2 class="h2-heading heading_color">Veetuku Oru Vingyani</h2>

<p>Veetukku Oru Vingyani competition was organized by Puthiya Thalaimurai TV channel. It was a district level Science Exhibition competition. This competition was held at MOUNT ZION SILVER JUBILEE Matric. Hr. Sec. School, Thenkarai. </p>

<p>More than 30 schools participated in this competition. Around 350 students exhibited their exhibits. From our school 7 students took part and exhibited their exhibits.</p>

<p>Master M. Deena Thayalan and K.G. Anilesh Benjamin of VIII standard exhibited their project AUTOMATIC RAILWAY GATE SYSTEM. This project got lots of appreciation from the judges and the visitors. The District Revenue Officer was the Chief Guest of the function, who congratulated our students and gave away the prize. This project demonstrated how to control accidents in the railway level crossings. </p>

<p>Our school was the only school from in and around Karaikudi area, which got 3rd Prize in the District. </p>

<p>Other exhibits prepared by our students were WATER LEVEL INDICATOR by Master M. Surya and M. Nikhil Meyyappan, which got consolation prize and ANTI THEFT ALARM by Master A.B.Jeya Yogesh and A.L. Sitarth.</p>
