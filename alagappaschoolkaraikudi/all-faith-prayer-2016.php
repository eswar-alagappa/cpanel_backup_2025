<h2 class="h2-heading heading_color">Sarva Matha Prarthana (All Faith Prayer)</h2>
<p class="text-bold text-center"><strong>Prayer is a privilege and the soul's sincere desire.</strong> </p>
<p>All Faith Prayer was orgranised for X and XII standard students who are going to appear for the Public Examination. The main aim of the prayer was to invoke the blessings of the God that will remove all the hindrances and make them face the Public Exam with confidence. This also shows the Unity in Diversity, in our school we pay respect for all religions.  </p>
<p>The prayer was conducted by the Priests of All the religions. </p>
<p>Priest Ayyappa Swamigal of Sri Maha Ugra Pratiyangira Devi Temple was invited for Hindu Dharma, Father Dinesh of Infant Jesus Church was invited to represent Christianity, Aalim Abdullah of Senjai Mosque was invited to represent the Muslim religion. </p>
<p>Our students welcomed the Priests by putting  flowers on their feet, as they are the Gurus of all religion. The prayer started with lighting the traditional Kuthu Vilakku by the Priests of all the religions. Our Principal, Mr. Kumaran Ganesan adorned Shawl to all the Priests and welcomed them. </p>
<p>All of them stressed on one matter viz., to give respect to parents. Once the students pay respect to parents and obey them, the students will surely inculcate good habits in them. They will be put on the write path which will take their destiny. </p>
<p>They also asked the students to maintain a friendly relationship with their teachers. When they are friendly with their teachers they will be benefited.</p>
<p>All Priests quoted live examples and they also told interesting stories which had a very good moral value. </p>
<p>Finally they blessed our students. Mantras were chanted by Sri Ayyappa Swamigal, Father Dinesh read Bible, and Aalim Abdulla read the Holy Quran. The students closed their eyes and meditated. </p>
<p>In a nutshell the prayer had very positive vibrations in our school and has brought us closer to our goals. </p>
