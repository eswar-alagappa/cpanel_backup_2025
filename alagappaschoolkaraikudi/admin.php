        <h2>Attendance</h2>
        <p>Students are expected to attend school on all working days from Monday to Saturday.</p>
        <p>The student should be present in the school at 8.30 AM every school day to attend the General Assembly and depart after school hours.</p>
        <p>If a student is absent from school due to illness or other unavoidable reasons a leave letter has to be sent to the class teacher duly signed by the parent / guardian. If such absence exceeds three days the leave letter must be addressed to the Principal accompanied by a medical certificate.</p>
        <p>In case of repeated absence or prolonged absence (exceeding 10 days) without any leave letter, the student's name will be removed from the rolls.</p>
      </div>
      <div id="content11" style="display:none;">
        <h2>Examinations</h2>
        <p>Unit tests will be conducted every Month for each subject. Mid-Term tests, Quarterly, Half yearly and Annual Examinations will be conducted for all classes.</p>
        <p>After each of the above examinations, except the Annual Examination and Unit tests, the progress cards will be issued to the parents on open days mentioned in the calendar, notifying the marks obtained by the student and also with the remarks of the class teacher and the Principal about the students' general attainments, aptitude, behavior and the fields of improvement.</p>
        <p>The progress cards should be returned to the school within three days of issue, signed by the parent or guardian, failing which a fine of Rs. 5/- per day will be collected.</p>
        <p>Students with 90% of attendance alone will be allowed to appear for the Public Examination in Std X and XII and Students with less than 85% attendance will not be promoted to other classes but for valid reasons.</p>
        <p>Progress cards spoilt or lost must be replaced by a fresh one on.</p>
