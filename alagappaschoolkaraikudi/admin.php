        <h2>Criteria for Admission</h2>
        <p> Admission will be made as per regulations established by the State Govt. of Tamil Nadu</p>
        <p> A student seeking admission into any class should produce a valid Transfer Certificate (T.C) from the previous school as well as the Corporation Birth Certificate and Community Certificate.</p>
        <p> Admissions for Classes IX to XI will be made only on production of Transfer Certificate attested by the Education Officer and Mark sheets from recognized schools.</p>
        <p> Students of CBSE pattern, Anglo Indian pattern, Tamil Nadu General Pattern and other Patterns should get their T.C. countersigned by the Educational Officer of the system.</p>
        <p> It also applies for students going from here to other schools and states.</p>
        <p> Students who are to be admitted in the middle of the year have to pay the tuition fees along with the Special Fees from June onwards.</p>
        <p> Application for admission must be made in the prescribed form which can be obtained from the school office on payment of Rs.100/- in person or can be downloaded (an addition of Rs100/- has to be remitted along with fees).</p>
