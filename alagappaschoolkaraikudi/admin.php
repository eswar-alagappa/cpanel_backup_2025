        <h2>School Rules & Regulations</h2>
        <ul>
          <li> Pupils must be in school by 9.00 AM to attend assembly every morning. Late comers will not be permitted to attend school. </li>
          <li> No pupil shall be absent without prior permission except during unforeseen circumstances. In such a case application for leave shall be sent to the class teacher so as to reach the office not later than 10.00 AM on the day of absence. </li>
          <li> Application of leave of absence shall be made on behalf of the people by the parent or guardian and addressed to the class teacher for the period not exceeding 3 days and to the Principal for a longer period. </li>
          <li> Pupils applying for sick leave are required to produce medical certificate. </li>
          <li> Every pupil shall attend school for not less than 85% of the working days in the academic year. </li>
          <li> White uniform should be worn by all pupils on every Monday and any other day specified. </li>
          <li> All notebooks and textbooks must be neatly covered and labeled throughout the year and school calendar must be brought to the school everyday. </li>
          <li> Periodical progress report must be collected by the parents or guardians to apprise them of the pupils conduct and work at school. </li>
