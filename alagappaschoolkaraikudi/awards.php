<h2 class="h2-heading heading_color">Awarding X and XII standard toppers in Quarterly Examination</h2>

<p>X and XII standard toppers were awarded with prizes to encourage them. For this purpose a special function was organized by us in our school auditorium. On this special occasion Dr. S. Ramakrishnan was the Chief Guest.</p>

<p>Dr. S. Ramakrishnan is the Trustee of Dr. Alagappa Chettiar Educational Trust. He is the retired Special Officer of Alagappa University and also the former Director of KIT and KIM. He was instrumental in establishing many of the institutions under the banner of Alagappa University.</p>

<p>The function started with Prayer by our students. First three Overall Rank holders of X and XII standards were given the prizes then followed by rank holders of each section.</p>

<p>The Chief Guest of the function Dr. S. Ramakrishnan motivated the students with his thought provoking speech. He in his speech highlighted the role played by our founder Dr. Alagappa Chettiar for the establishment of Institutes in Karaikudi and also reiterated that without him Karaikudi would not have become a renowned town in the state. He also reminded the students that they are studying in a well reputed Institution and they must work hard to bring laurels to it. 
</p>

<p>He also appreciated the Principal of the School, Mr. Kumaran Ganesan for putting strenuous efforts for the overall development of the school and producing good results in the Board Examinations.</p>

<p>Finally the Principal extended his thanks to the Chief Guest for his visit and for encouraging the students. </p>

