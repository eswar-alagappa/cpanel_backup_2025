<h2 class="h2-heading heading_color">VINAYAGA CHATHURTHI AND TEACHERS DAY CELEBRATION</h2>

<p>Vinayaga Chathurthi and Teachers’ Day was celebrated in our school campus on 3rd September 2016. On this occasion many colourful cultural events were performed by the students.</p>

<p>Teachers’ Day celebrations started in the morning with students greeting the teachers by presenting them Rose. Teachers accepted their greetings and blessed them. On 3rd morning the teachers performed the prayer. Teachers were allotted duty to sing prayer song, present news, say thought for the day, recite  Thirukural with explanation, Bible reading, Quaran reading and Bhagawath Geetha reading etc as done by the students in the normal days.</p>

<p>As a part of CCA activity, from 10.00 AM to 11.30 AM Floral Arrangements and Vegetable Carving competition was conducted for standards VI to IX and XI. Students exhibited their talents by actively involving in this competition.  </p>

<p>In the evening a colorful cultural programme was arranged by the students of Interact Club of Alagappa Matric. The celebrations started with cutting of Cake by our Principal, Mr. Kumaran Ganesan.  </p>

<p>A colourful folk dance was performed by the students of VI and VII. It was followed by reading a poem by our student Ms. Shalini of XI standard praising her teachers. After that a comedy skit was presented by the students of XI standard followed by AD-ACT. Our student 8th grade student Nilofer, quarter finalist in super singer, sang a melodious song on this occasion. Nilofer is also runner up in super singer quarter finals.</p>

<p>At 4.30 PM an enrichment programme for teachers was conducted in our school auditorium. Prof. Janakiraman a well known orator and motivator, a retired professor of Sevugan Annamalai College, Devakottai, gave a lecture on this occasion. He listed out various good qualities that a teacher must posses with some live humorous examples. Teachers enjoyed the session and gained positive attitude from this session. </p>

<p>In the evening pooja was performed to Lord Ganesha. Mothakam was offered as prasatham to Lord Ganesha after that it was distributed to all the teachers. Also teachers were presented with a Teachers Day special memento. Memento was distributed by the chief guest of the function Prof. Janakiraman. Finally the function came to a close with delicious food item served to everyone. </p>
